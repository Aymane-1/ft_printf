#include "libft.h"

void	ft_hexa_up(int n)
{
	long nb;
	char hex[16] = "0123456789ABCDEF";

	nb = n;

	if (nb > 15)
	{
		ft_hexa_up(nb / 16);
		ft_hexa_up(nb % 16);
	}
	else
		ft_putchar(hex[nb]);
	}

